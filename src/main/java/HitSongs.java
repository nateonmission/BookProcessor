public class HitSongs
{

    public String date;
    public String position;
    public String song;
    public String performer;
    public String weeksOnChart;


    public HitSongs
            (
                    String date,
                    String position,
                    String song,
                    String performer,
                    String weeksOnChart
            )
    {
        this.date = date;
        this.position = position;
        this.song = song;
        this.performer = performer;
        this.weeksOnChart = weeksOnChart;
    }

}


